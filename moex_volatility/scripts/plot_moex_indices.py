# Fetch data from MOEX ISS API
# ═══════════════════════════════════════════════════════════════════
def fetch_index(ticker, start_date="2022-01-01", end_date="2026-02-20"):
    """Fetch daily index data from MOEX ISS with pagination."""
    base_url = (
        f"https://iss.moex.com/iss/history/engines/stock/markets/index/"
        f"securities/{ticker}.json?from={start_date}&till={end_date}"
        f"&iss.meta=off"
    )
    all_rows = []
    offset = 0
    while True:
        url = f"{base_url}&start={offset}"
        print(f"  Fetching {ticker} offset={offset}...")
        with urllib.request.urlopen(url) as resp:
            data = json.loads(resp.read().decode("utf-8"))

        rows = data["history"]["data"]
        if not rows:
            break
        all_rows.extend(rows)
        offset += 100

    # Find column indices
    columns = data["history"]["columns"]
    date_idx = columns.index("TRADEDATE")
    close_idx = columns.index("CLOSE")

    dates = []
    closes = []
    for row in all_rows:
        if row[close_idx] is not None and row[close_idx] > 0:
            dates.append(datetime.datetime.strptime(row[date_idx], "%Y-%m-%d"))
            closes.append(float(row[close_idx]))

    print(f"  {ticker}: {len(dates)} trading days loaded")
    return dates, closes


