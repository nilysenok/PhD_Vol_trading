# Build chart
# ═══════════════════════════════════════════════════════════════════
def ru_date_formatter(x, pos):
    """Format date tick as 'Мар 2022' in Russian."""
    months_ru = {1: "Янв", 2: "Фев", 3: "Мар", 4: "Апр", 5: "Май", 6: "Июн",
                 7: "Июл", 8: "Авг", 9: "Сен", 10: "Окт", 11: "Ноя", 12: "Дек"}
    dt = mdates.num2date(x)
    return f"{months_ru[dt.month]} {dt.year}"


def plot_indices(imoex_dates, imoex_close, rtsi_dates, rtsi_close):
    import numpy as np
    fig, ax1 = plt.subplots(figsize=(13, 5))

    c_imoex = "#1A3C6E"
    c_rtsi  = "#A63A2D"

    # Convert dates to numeric for polyfit
    imoex_num = mdates.date2num(imoex_dates)
    rtsi_num = mdates.date2num(rtsi_dates)

    # IMOEX
    ax1.plot(imoex_dates, imoex_close, color=c_imoex, linewidth=1.4,
             label="Индекс МосБиржи (IMOEX)", zorder=3)
    ax1.set_ylabel("Индекс МосБиржи (IMOEX)", fontsize=12, color=c_imoex)
    ax1.tick_params(axis="y", labelsize=11, labelcolor=c_imoex)

    # RTSI
    ax2 = ax1.twinx()
    ax2.plot(rtsi_dates, rtsi_close, color=c_rtsi, linewidth=1.4,
             label="Индекс РТС (RTSI)", zorder=3)
    ax2.set_ylabel("Индекс РТС (RTSI)", fontsize=12, color=c_rtsi)
    ax2.tick_params(axis="y", labelsize=11, labelcolor=c_rtsi)

    # Title
    ax1.set_title("Динамика индексов IMOEX и RTSI (2022\u20132026)",
                  fontsize=14, pad=10)

    # X axis — Russian months, horizontal, every 6 months
    ax1.xaxis.set_major_formatter(mticker.FuncFormatter(ru_date_formatter))
    ax1.xaxis.set_major_locator(mdates.MonthLocator(interval=6))
    ax1.tick_params(axis="x", labelsize=10, rotation=0)

    # Grid
    ax1.grid(True, alpha=0.25, color="#cccccc")
    ax1.set_axisbelow(True)

    # Spines
    ax1.spines["top"].set_visible(False)
    ax2.spines["top"].set_visible(False)
    ax1.spines["left"].set_color(c_imoex)
    ax2.spines["right"].set_color(c_rtsi)

    # Legend
    lines1, labels1 = ax1.get_legend_handles_labels()
    lines2, labels2 = ax2.get_legend_handles_labels()
    ax1.legend(lines1 + lines2, labels1 + labels2,
               loc="lower right", framealpha=0.95, edgecolor="#cccccc",
               fontsize=11)

    fig.tight_layout()
    out_path = os.path.join(OUT, "moex_indices_2022_2026.png")
    fig.savefig(out_path, dpi=300, bbox_inches="tight")
    plt.close(fig)
    print(f"\n  ✓ {out_path}")


